#include "basemainwindow.h"

BaseMainWindow::BaseMainWindow(QWidget *parent) : QMainWindow(parent) {}

void BaseMainWindow::keyPressEvent(QKeyEvent *event) {
    if (event->key() == Qt::Key_F1) {
        showUserGuide();
    } else {
        QMainWindow::keyPressEvent(event);
    }
}

void BaseMainWindow::showUserGuide() {
    QString guideText = R"(
<b>ДОБРО ПОЖАЛОВАТЬ В ПРИЛОЖЕНИЕ «БАЗА ЗНАНИЙ ПО ФЭНТЕЗИ-ВСЕЛЕННЫМ»</b><br><br>

Это программа, с помощью которой вы можете изучать сказочные и волшебные миры.<br><br>

<b>КАК НАЧАТЬ:</b><br>
• Когда вы запускаете приложение, появится окно входа.<br>
• Если у вас уже есть аккаунт, введите ваш логин и пароль и нажмите «Войти».<br>
• Если аккаунта ещё нет, нажмите «Регистрация», придумайте логин и пароль, и нажмите «Зарегистрироваться».<br><br>

<b>ГЛАВНАЯ СТРАНИЦА:</b><br>
Здесь есть несколько разделов. Чтобы перейти — просто нажмите кнопку.<br><br>

<b>РАЗДЕЛ «КАТАЛОГ»:</b><br>
• Здесь собраны вселенные: Гарри Поттер, Властелин колец, Аватар и другие.<br>
• Нажмите на любую вселенную — вы попадёте в подробное описание с картинками и интересными фактами.<br>
• Чтобы добавить понравившуюся вселенную в список любимых — нажмите на сердечко.<br>
• Можно найти нужную вселенную через строку поиска или выбрать по темам (магия, драконы и т.д.).<br><br>

<b>РАЗДЕЛ «ТЕСТЫ»:</b><br>
• Вы можете пройти интересные тесты по любимым вселенным.<br>
• После прохождения вы увидите результат.<br><br>

<b>РАЗДЕЛ «ЛЮБИМОЕ»:</b><br>
• Здесь отображаются все вселенные, которые вы добавили в избранное (нажав на сердечко).<br>
• Если какая-то вселенная больше не нравится — можно убрать её отсюда.<br><br>

<b>ВАЖНО:</b><br>
Все данные сохраняются автоматически. Можно не бояться закрыть программу — всё останется.<br><br>

<b>ДЛЯ ПОЛУЧЕНИЯ СПРАВКИ В ЛЮБОЕ ВРЕМЯ — НАЖМИТЕ КЛАВИШУ F1.</b>
)";

    QMessageBox::information(this, "Руководство пользователя", guideText);
}

